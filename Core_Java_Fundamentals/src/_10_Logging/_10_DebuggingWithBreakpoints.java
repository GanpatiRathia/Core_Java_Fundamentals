package _10_Logging;

public class _10_DebuggingWithBreakpoints {
    public static void main(String[] args) {
        int[] numbers = {10, 20, 30, 40};
        for (int i = 0; i < numbers.length; i++) {
            System.out.println("Index: " + i + " Value: " + numbers[i]);
        }
    }
}
